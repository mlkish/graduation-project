create table public.messages
(
    id         uuid primary key                                      default gen_random_uuid() not null,
    chat_id    uuid references chats (id) on delete cascade not null,
    user_id    uuid                                         not null references auth.users on delete cascade,
    role       text check (role in ('user', 'assistant'))   not null,
    content    text                                         not null,
    created_at timestamp with time zone                     not null default now()
);

alter table public.messages ENABLE row LEVEL SECURITY;

-- The caller may only insert their own messages, and only into a chat they own.
-- RLS is the real boundary here: the browser holds the anon key and can call
-- PostgREST directly, so we must not trust the edge function to scope writes.
create
policy "Enable insert for users based on user_id" on public.messages as PERMISSIVE for INSERT to authenticated
with
  check (
    (
    select
    auth.uid ()
    ) = user_id
    and exists (
    select
    1
    from public.chats c
    where c.id = chat_id
    and c.user_id = (
    select
    auth.uid ()
    )
    )
    );

create
policy "Enable select for users based on user_id" on public.messages as PERMISSIVE for
select
    to authenticated using (
    (
    select
    auth.uid ()
    ) = user_id
    );

-- Persist a full turn (create the chat on first message, then both messages)
-- atomically in one transaction. SECURITY INVOKER keeps the RLS policies above
-- in force, and clock_timestamp() gives the two messages distinct, ordered
-- created_at values so history is always read back in the right order.
create
or replace function public.save_turn (
  p_chat_id uuid,
  p_title text,
  p_user_content text,
  p_assistant_content text
) returns uuid language plpgsql security invoker as $$
declare
  v_uid uuid := auth.uid();
  v_chat_id uuid := p_chat_id;
begin
  if v_uid is null then
    raise exception 'Not authenticated';
  end if;

  if v_chat_id is null then
    insert into public.chats (user_id, title)
    values (v_uid, p_title)
    returning id into v_chat_id;
  elsif not exists (
    select 1 from public.chats c where c.id = v_chat_id and c.user_id = v_uid
  ) then
    raise exception 'Chat not found';
  end if;

  insert into public.messages (chat_id, user_id, role, content, created_at)
  values (v_chat_id, v_uid, 'user', p_user_content, clock_timestamp());

  insert into public.messages (chat_id, user_id, role, content, created_at)
  values (v_chat_id, v_uid, 'assistant', p_assistant_content, clock_timestamp());

  return v_chat_id;
end;
$$;
