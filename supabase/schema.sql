create extension if not exists vector
with
  schema extensions;

-- ---------------------------------------------------------------------------
-- Per-user knowledge base
--
-- Each user uploads their own files. Files live in a private Storage bucket
-- under a folder named after the user's id ("<uid>/<filename>"), and the text
-- chunks + embeddings live in public.documents scoped by user_id. The chat
-- only ever retrieves context from the calling user's own documents.
-- ---------------------------------------------------------------------------

drop function if exists public.match_documents (vector, float, int);
drop function if exists public.match_documents (vector, float, int, uuid);
drop table if exists public.documents;

create table public.documents
(
    id         bigint generated by default as identity not null,
    user_id    uuid                                    not null references auth.users on delete cascade,
    file_name  text                                    not null,
    content    text                                    not null,
    embedding  vector (1536)                           not null,
    created_at timestamp with time zone                not null default now(),
    primary key (id)
);

-- Approximate nearest-neighbour index for fast cosine similarity search.
create index documents_embedding_idx
    on public.documents using hnsw (embedding vector_cosine_ops);

-- Help the planner pre-filter to the calling user's rows.
create index documents_user_id_idx on public.documents (user_id);
create index documents_user_file_idx on public.documents (user_id, file_name);

alter table public.documents enable row level security;

create
policy "Users can read own documents" on public.documents as PERMISSIVE for
select
    to authenticated using (
    (
    select
    auth.uid ()
    ) = user_id
    );

create
policy "Users can insert own documents" on public.documents as PERMISSIVE for INSERT to authenticated
with
  check (
    (
    select
    auth.uid ()
    ) = user_id
    );

create
policy "Users can delete own documents" on public.documents as PERMISSIVE for DELETE to authenticated using (
    (
    select
    auth.uid ()
    ) = user_id
    );

-- Similarity search scoped to a single user's documents. SECURITY INVOKER
-- (the default) means the documents RLS policies above also apply, so a user
-- can never retrieve another user's chunks even if filter_user_id were wrong.
-- Returns file_name so the chat can attribute each excerpt to its source.
-- The extra drop keeps this block runnable on its own: "create or replace"
-- cannot change an existing function's return type.
drop function if exists public.match_documents (vector, float, int, uuid);

create
or replace function match_documents (
  query_embedding vector (1536),
  match_threshold float,
  match_count int,
  filter_user_id uuid
) returns table (id bigint, file_name text, content text, similarity float) language sql stable as $$
select documents.id,
       documents.file_name,
       documents.content,
       1 - (documents.embedding <=> query_embedding) as similarity
from documents
where documents.user_id = filter_user_id
  and 1 - (documents.embedding <=> query_embedding) > match_threshold
order by documents.embedding <=> query_embedding
    limit match_count;
$$;

-- ---------------------------------------------------------------------------
-- Private Storage bucket for user files + per-user folder access policies.
-- The first path segment of every object name must equal the user's id.
-- ---------------------------------------------------------------------------

-- Private bucket. Force public = false even if it already exists, otherwise a
-- pre-existing public bucket would serve every user's files over public URLs,
-- bypassing the object policies below.
insert into storage.buckets (id, name, public)
values ('documents', 'documents', false)
on conflict (id) do update set public = false;

-- storage.objects is a shared system table (not dropped/recreated above), so
-- make these policies re-runnable.
drop policy if exists "Users can read own files" on storage.objects;
drop policy if exists "Users can upload own files" on storage.objects;
drop policy if exists "Users can update own files" on storage.objects;
drop policy if exists "Users can delete own files" on storage.objects;

create
policy "Users can read own files" on storage.objects as PERMISSIVE for
select
    to authenticated using (
    bucket_id = 'documents'
    and (storage.foldername (name)) [1] = (
    select
    auth.uid ()
    ) :: text
    );

create
policy "Users can upload own files" on storage.objects as PERMISSIVE for INSERT to authenticated
with
  check (
    bucket_id = 'documents'
    and (storage.foldername (name)) [1] = (
    select
    auth.uid ()
    ) :: text
    );

create
policy "Users can update own files" on storage.objects as PERMISSIVE for
UPDATE to authenticated using (
    bucket_id = 'documents'
